.MODEL SMALL

.STACK 100H

.DATA

 Num1 db "MEDIAN Value is: $"
       
.CODE
MAIN PROC

;initizlize DS

mov ax,@DATA
mov ds,ax

MOV AH, 01H
INT 21H
MOV BL,AL
SUB BL,30H

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL, 0DH
INT 21H

MOV AH, 01H
INT 21H
MOV BH,AL
SUB BH,30H

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL, 0DH
INT 21H

MOV AH, 01H
INT 21H
MOV CL,AL
SUB CL,30H

MOV AH,2
MOV DL,0AH
INT 21H
MOV DL, 0DH
INT 21H

CMP BL,BH
JG NEXT
JMP LATER

NEXT:
 CMP BL,CL
 JL NEXT_2
 CMP BH,CL
 JG NEXT_3
 MOV AL,CL
 JMP FINAL
 

NEXT_2:
MOV AL,BL
JMP FINAL

NEXT_3:
MOV AL,BH
JMP FINAL  

LATER:
CMP BL,CL
JG NEXT_2
CMP BH,CL
JL NEXT_3
MOV AL,CL
JMP FINAL


FINAL:
MOV BL,AL

LEA DX, Num1
MOV AH, 09H
INT 21H

MOV DL,BL
ADD DL, 30H
MOV AH, 02H   
INT 21H      

MAIN ENDP
   END MAIN